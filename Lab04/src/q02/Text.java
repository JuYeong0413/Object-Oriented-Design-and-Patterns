package q02;

public class Text {
	String text;
	String author;
	int length;
	
	String getText() { return text; }
	void setText(String s) { this.text = s; }
	String getAuthor() { return author; }
	void setAuthor(String s) { this.author = s; }
	int getLength() { return length; }
	void setLength(int k) { this.length = k; }
	
}
